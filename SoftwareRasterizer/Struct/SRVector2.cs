namespace SoftwareRasterizer.Struct;

public struct SRVector2
{
    public float X;
    public float Y;
 
    public SRVector2(float x, float y)
    {
        X = x;
        Y = y;
    }
}
public struct SRVector2Int
{
    public int X;
    public int Y;
 
    public SRVector2Int(int x, int y)
    {
        X = x;
        Y = y;
    }
}